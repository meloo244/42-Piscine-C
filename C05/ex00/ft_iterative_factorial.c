int	ft_iterative_factorial(int nb)
{
	int	n;

	n = nb;
	if (nb == 0)
		return (1);
	if (nb < 0)
		return (0);
	while (n != 1)
	{
		n = n - 1;
		nb = nb * n;
	}
	return (nb);
}
